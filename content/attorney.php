	<p align="left"> John Marschner Rickel is a licensed attorney (#P19432) and is Managing Director of the firm. He graduated from the Detroit Public Schools in 1957, he received his Bachelor of Arts degree in 1960 from the University of Michigan, and his Juris Doctor from the University of Michigan Law School in 1963. </p>
	<p align="left"> Upon graduation from law school and being admitted to the Michigan State Bar, Mr. Rickel was an Instructor at Wayne State University Law School for three years, as well as active in the Counsel to his family's multi-state business, of which he became Treasurer in 1969. He also attended the University of Michigan Graduate School of Business Administration and joined Touche Ross & Co. (now Deloitte & Touche, C.P.A.s) in 1969, becoming a Certified Public Accountant (#7195) admitted to practice in 1972. He served on both the audit and tax staffs of that firm until 1975, when he joined the Butzel, Long, Gust, Klein and Van Zile law firm in private practice. Mr. Rickel was a founder of Rickel & Baun, P.C., and its predecessor firms in 1977, as well as John M. Rickel, C.P.A., P.C. Mr. Rickel specialized in real estate, tax, S.E.C., corporate, and contract law, as well as handling family, insurance and agency law problems. He worked in planning, structuring, negotiating, and litigating business and personal financial transactions, and he dealt with both publicly and privately held corporations. His diversified corporate and individual clientele has taken him into significant involvement in corporate, probate, divorce, commercial, collection, real estate and tax law litigation. He is past President/Treasurer and past Chairman of the Board of Directors of the Grosse Pointe Cable Television system. </p>	
	<p align="left"> Mr. Rickel periodically teaches tax, accounting, auditing, and bankruptcy seminars for the Michigan Association of Certified Public Accountants, for whom he sits as Vice Chair of the Professional Development Committee (which is responsible for all MACPA Continuing Professional Education - representing 72% of MACPA budget), and he is a member of the MACPA Professional Education faculty.  Mr. Rickel is a member of the twenty person State of Michigan Association of Certified Public Accountants Board of Directors. </p>	
	<p align="left"> Mr. Rickel served for 2 years as a Special Investigator for the Attorney Grievance Commission of the State Bar of Michigan. He is a member of the State Bar of Michigan and American Institute of Certified Public Accountants. He is a Chartered Financial Analyst (#7694). </p>
	<p align="left"> Mr. Rickel is President, Chairman of the Board of Directors, and Chairman of Licensing Advocacy, of the Investment Analysts Society of Detroit, the Michigan affiliate of the international Association for Investment Management and Research (AIMR). He is a member of the Board of Directors of a number of Michigan corporations. Mr. Rickel is admitted to legal practice in Michigan State Courts, U.S. District Court, U.S. Court of Appeals - Sixth Circuit, U.S. Tax Court, and the United States Supreme Court. He is a life member of the Economic Club of Detroit, and is a past President, past Treasurer and past Board Member of the Grosse Pointe War Memorial Association, Grosse Pointe's community center, a past Board Member of the Grosse Pointe Historical Society and the Circumnavigator's Club, current Treasurer (and a past Board Member) of the Presidents' Organization, Inc. (the Detroit YPO alumni organization), and a past Board Member and past Corporate Secretary of the University Club of Detroit. He is also a licensed instrument pilot. Mr. Rickel has attended the YPO Executive Management Seminar at the Stanford University Graduate Business School, and the YPO Advanced Management Seminar at the Harvard University Business School. </p>
